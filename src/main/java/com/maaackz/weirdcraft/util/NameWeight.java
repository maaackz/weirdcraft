package com.maaackz.weirdcraft.util;

public class NameWeight {
    private final String name;
    private final int weight;

    public NameWeight(String name, int weight) {
        this.name = name;
        this.weight = weight;
    }

    public String getName() {
        return name;
    }

    public int getWeight() {
        return weight;
    }
}
